//-----------------------------------------------------------------------------
//	@brief	インクルード
//-----------------------------------------------------------------------------
#include "UIScore.h"

//-----------------------------------------------------------------------------
// @brief	コンストラクタ
//-----------------------------------------------------------------------------
UIScore::UIScore()
{
	m_pos = VGet(1450.0f, 10.0f, 0.0f);
	m_score = 0.0f;
	m_targetScore = 0.0f;
}

//-----------------------------------------------------------------------------
// @brief	 デストラクタ
//-----------------------------------------------------------------------------
UIScore::~UIScore()
{
	// 処理なし
}

//-----------------------------------------------------------------------------
// @brief	更新
//-----------------------------------------------------------------------------
void UIScore::Update()
{
	//	ラーフを使い、スコアを求める
	const float ignore = 1.0f;
	const float lerpSpeed = 0.2f;
	m_score = CommonFunction::Lerp(m_score, m_targetScore, lerpSpeed, ignore);

	//	スコアを文字列にする
	sprintf(m_scoreText,"%010d", (int)m_score);
}

//-----------------------------------------------------------------------------
// @brief	描画
//-----------------------------------------------------------------------------
void UIScore::Draw()
{
	//	スコアの表示
	FONT->Draw((int)m_pos.x, (int)m_pos.y, m_scoreText);
}

//-----------------------------------------------------------------------------
// @brief	スコアのリセット
//-----------------------------------------------------------------------------
void UIScore::ScoreReset()
{
	m_score = 0.0f;
	m_targetScore = 0.0f;
}
